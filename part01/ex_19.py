"""
A program which asks the user for four numbers and then prints out the sum and the mean of the numbers.
"""
number01 = int(input("Number 1:"))
number02 = int(input("Number 2:"))
number03 = int(input("Number 3:"))
number04 = int(input("Number 4:"))
print(f"The sum of the numbers is {number01 + number02 + number03 + number04} and the mean is {(number01 + number02 + number03 + number04)/4}")
