"""
A function named squared, which takes a string argument and an integer argument, and prints out a square of characters
"""

def squared(characters, size):
    i = 0
    row = ""
    while i < size * size:
        if i > 0 and i % size == 0:
            print(row)
            row = ""
        row += characters[i % len(characters)]
        i += 1
    print(row)
